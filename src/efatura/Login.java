    void aceder (String usuario,String pass) throws ClassNotFoundException{
        String cap = "";
        String sql = "SELECT * FROM utilizador WHERE username='"+ usuario +"' && password='"+ pass +"'";
        //Connection conn = null;
        //PreparedStatement pstmt = null;
        try {
            //Class.forName("com.mysql.jdbc.Driver");
            //conn=DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/efatura","root","12345678");
            conecta.openConnectionMySql();
            conecta.stmt = conecta.con.createStatement();
            //Statement st = conn.createStatement();
            ResultSet rs = conecta.stmt.executeQuery(sql);
            while(rs.next())
            {
                cap = rs.getString("tipousuario");
            }
            if(cap.equals("Administrador") || cap.equals("Utilizador Normal")){
                this.setVisible(false);
                JOptionPane.showMessageDialog(null, "Bem-Vindo");
                Menu menu = new Menu();
                menu.setVisible(true);
                menu.pack();
                
            }
            if((!cap.equals("Administrador"))&&(!cap.equals("Utilizador Normal"))){
                
                JOptionPane.showMessageDialog(this, "NÃ£o exitem os seus dados");
            }
        
    }
        catch(SQLException ex){
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
